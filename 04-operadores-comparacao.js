// Operadores de Comparação são usados para comparar valores// o resultado de uma comparação é SEMPRE um valor booleando: true ou false

// 1 - igual ==
console.log(5 == 5)
console.log("5" == 5)
console.log(5 == "5")


// 2 - igual estrito ===
console.log(5 === 5)
console.log("5" === 5)
console.log(5 === "5")

// 3 - maior que >
console.log(10 > 5)
console.log(5 > 10)

// 4 - menor que <
console.log(5 < 10)
console.log(10 < 5)

// 5 - maior ou igual >=
console.log(10 >= 5)
console.log(5 >= 10)
console.log(5 >= 5)

// 6 - menor ou igual <=
console.log(5 <= 10)
console.log(10 <= 5)
console.log(5 <= 5)